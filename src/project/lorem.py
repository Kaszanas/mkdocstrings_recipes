def string_addition_is_addiction(str_0:str, str_1:str) -> str:
    """
    Concatenates two strings.

    Parameters
    ----------
    str_0 : str
        Takes a string.
    str_1 : str
        Takes a string.

    Returns
    -------
    str
        Returns the concatenation of the two strings.
    """
    return str_0 + str_1